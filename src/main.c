size_t bootrom_bin_len = 0xFF;

int main()
{
    //runTests();
    resetBus();
    bus_t *pBus = pGetBusPtr();
    const cpu_t *pCpu = getCpuObject();
    
    bool instrHit[256] = {0};

    resetCpu();
    ppuInit();
    initRenderWindow();

    loadRom("Tetris.gb");

    // overlay with bootrom
    memcpy(&pBus->bus[0], &bootrom_bin[0], bootrom_bin_len + 1);

    while(true)
    {
        int mCycles = 0;
        uint8_t opcode = pBus->bus[pCpu->reg16.pc];

        //printf("executing 0x%02x at pc 0x%02x\n", pBus->bus[pCpu->reg16.pc], pCpu->reg16.pc);

        if (pCpu->ime)
        {
            mCycles += handleInterrupts();
        }

        if ((pCpu->reg16.pc >= 0x100) && !instrHit[opcode])
        {
            instrHit[opcode] = true;
        }
        
        if (pCpu->reg16.pc == 0x237) // here it fails
        {
            __asm("nop");
        }

        mCycles += executeInstruction(pBus->bus[pCpu->reg16.pc]);
        int ppuCycles = mCycles * 4;

        handleTimers(mCycles);

        if (pBus->map.ioregs.disableBootrom)
        {
            unmapBootrom();
        }

        ppuLoop(ppuCycles); // 1 CPU cycle = 4 PPU cycles
    }
}